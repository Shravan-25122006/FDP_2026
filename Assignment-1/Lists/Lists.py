list = [1, 2, 3, 4]

# add at end
list.append(5)        
print(list)

# insert at index
list.insert(1, 10)    
print(list)

# remove value
list.remove(3)           
print(list)

# remove last
list.pop()            
print(list)

#sort list
list.sort()     
print(list)

# reverse list
list.reverse()
print(list)

# clear list
list.clear() 
print(list)         



