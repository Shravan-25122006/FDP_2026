s = "hello world"

print(s.upper())      # UPPERCASE
print(s.lower())      # lowercase 
print(s.capitalize()) # First letter capital
print(s.title())      # Each word capital 
print(s.replace("world","python")) 
print(s.split())      # Split into list
print(s.find("o"))    # Find index 
print(s.count("l"))   # Count occurrence
